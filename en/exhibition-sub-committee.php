                <h3>Organization of Exhibition</h3>
                
                <h4>Organizing “Hong Kong International Jewelry Manufacturers’ Show”</h4>
                
                <p>Since 1995, HKJMA has organized “Hong Kong International Jewelry Manufacturers’ Show” annually. It keeps a steady growth and becomes one of the largest Asian jewelry events at year-end, serving as an “immediate sales & delivery right” trading as well as information platform before the hottest selling seasons. Nowadays, the show becomes one of the Asian major jewelry buying events and serves as an immediate sales platform for global buyers.</p>
                
                <img src="../images/about-us/exhibition-sub-committee/exhibition-img-1.png" />
                <img src="../images/about-us/exhibition-sub-committee/exhibition-img-2.png" />
                
                <h4>Year Performance</h4>
                
                <p>Hong Kong International Jewelry Manufacturers’ Show 2012 was held at Hong Kong Convention & Exhibition Centre, Hall 1.</p>
                
                <h4>Offering the Excellent Opportunity at Right Time and Place</h4>
                <p>Hong Kong International Jewelry Manufacturers’ Show 2012 is a huge success with the expansion of 15% increase in overseas buyer attendance. In recent years, apart from Hong Kong manufacturers, the show attracted exhibitors from over 20 countries including China, Italy, Japan, Belgium, Taiwan, Israel, Thailand and Korea etc. The 4 days show featured nearly 565 quality jewelry manufacturers with 800 booths and attracted 26,400 buyers, in which the number of mainland China buyers has increased 30%. Buyers were keen to attend this year-end jewelry delivery show to do last minute purchase to stock up before the holiday season.</p>
                
                <p>In the years to come, 2013 show will continue to focus on attracting buyers from mainland China & Asia and continue to match quality exhibitors with quality buyers. </p>
                
                
