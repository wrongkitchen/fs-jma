                <p>HKJMA has been established for 25 years into a ripe organization with a strong foothold for future service enhancement. Being a communicative platform for our members, we would take an active role in responding to members’ opinions and communicating effectively to improve the existing services. For example, we value every opinion gathered from questionnaires received after each show. We have an open attitude in accepting suggestions to make our services tailor-make for our members.</p>
                
				<p>Attributable to the vast development opportunities in China, we have been actively developing mainland business in recent years. Our Shenzhen office has been established nearly 10 years, providing latest information to mainland buyers and assisting members to promote their business. Predictably the mainland consumption model will shift from high-end to free consumption ranging from $3,000 - $5,000. More people in China can afford jewelry implies the surge in jewelry demand, bringing great opportunities for Hong Kong jewelry manufacturers. We will keep providing such services as business matching, organizing Hong Kong pavilion in mainland jewelry exhibitions, promotions to mainland chain stores and potential buyers, building a closer relationship with mainland jewelry associations, as well as organizing China VIP delegations to visit Hong Kong International Jewelry Manufacturers’ Show to tighten up the linkage between Hong Kong and China. It is estimated wealthy regions like Ningbo, Shandong, Hangzhou and South China will have greater potential development. Hong Kong’s spectacular designs can draw much attention from the regions.</p>

				<p>Given the support from Innovation and Technology Commission, Jewelry Shanghai 2013 could have greater capital for marketing and promotion to enhance the buyer awareness, numerous visitors and sales revenue have been increased. It is hoped that the government can keep on assisting jewelry industry to promote in different markets, backing up the whole industry. The number of exhibitors in Hong Kong Pavilion of this year’s JCK Las Vegas Show has already reached 203, making up 323 booths. Both the number of visitors and sales have surged. Thus, we will put more resources on this Show, getting more buyers to know Hong Kong jewelry. Considering the emerging market, the most difficult time in the Middle East market has passed and on the rise in recent years. We led Hong Kong jewelry manufacturers to participate in The 11th Gold and Jewelry Exhibition in Kuwait for the first time. Hong Kong pavilion was one of the biggest overseas pavilions there. We will keep paying attention to the global economy and act as a good platform in accordance to the market change, in order to assist members to seek more trading opportunities.</p>
                
