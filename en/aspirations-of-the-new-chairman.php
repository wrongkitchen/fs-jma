                <p>Hong Kong International Jewelry Manufacturers’ Show has been established over 20 years. It has become an essential year-end merchandizing time for buyers all over the world. The number of visitors has spiraled in recent years. People around the globe, especially South East Asia and mainland buyers grasp this opportunity to source stocks. Our Association thinks highly of this exhibition and keeps improving the services. Other than the usual International Designer Arena and China VIP Delegation, we also held the Miss Jewelry Hong Kong unprecedentedly. Last year, we even organized Hong Kong’s first jewelry industry summit - World Jewellery Industry Development Summit – Hong Kong to foresee the future development of jewelry industry. Our Association will continue improving our exhibition services, enriching the content to attract more buyers from different regions. </p>
                
